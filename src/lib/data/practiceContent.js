/**
 * 레벨별 추천 연습 내용 가져오기
 */
export function getRecommendedContent(level, type) {
	const content = practiceContent[level] || practiceContent.beginner;
	return type === 'vocabulary' ? content.vocabulary : content.sentences;
}

/**
 * 랜덤 연습 내용 가져오기
 */
export function getRandomContent(level, type, count = 3) {
	const allContent = getRecommendedContent(level, type);
	const shuffled = [...allContent].sort(() => Math.random() - 0.5);
	return shuffled.slice(0, Math.min(count, shuffled.length));
}

